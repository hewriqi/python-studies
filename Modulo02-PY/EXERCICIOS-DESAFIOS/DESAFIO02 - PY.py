"""
Faça um programa que tenha uma função que receba um texto
qualquer como parâmetro e mostre uma mensagem com tamanho adaptável.

Write a program that has a function that 
receive any text as a parameter and display 
a message with adaptable size.
"""


def escreva(msg):
    print('-' * len(msg))
    print(msg)
    print('-' * len(msg))


escreva('Henrique')
escreva('Neymaaaaarr Juniorrrrrrrr')
escreva('Brasil')
